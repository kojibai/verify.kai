if ('serviceWorker' in navigator) {
  window.addEventListener('load', () => {
    navigator.serviceWorker
      .register('/sw.js')
      .then((reg) => console.log('Kairos Service Worker registered:', reg))
      .catch((err) => console.error('Service Worker error:', err));
  });
}